package com.cqust.model;

public class Login {
	private int lo_id;
	private int id;
	private String password;
	private User user;
	public int getLo_id() {
		return lo_id;
	}
	public void setLo_id(int lo_id) {
		this.lo_id = lo_id;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public User getUser() {
		return user;
	}
	public void setUser(User user) {
		this.user = user;
	}
	
	
	
}
